#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
*op_add - agrega 2 numeros
*@a: numero1
*@b: numero2
*Return: resultado
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - resta 2 numeros
*@a: numero1
*@b: numero2
*Return: resultado
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiplica 2 numeros
*@a: numero1
*@b: numero2
*Return: resultado
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divide dos numeros
*@a: numero1
*@b: numero2
*Return: resultado
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - modulo de dos numeros
*@a: numero1
*@b: numero2
*Return: resultado
*/
int op_mod(int a, int b)
{
	return (a % b);
}
